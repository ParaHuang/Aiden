package day4;

public class Main1 {

	public static void main(String[] args) {
		//while
		//for
		for(int i=1 ; i<=5 ; i++) {
			System.out.println(i);
		}
		
		
		/*
		 //while
		 while(condition){
		 	operations;
		 }
		 //do...while... : it will execute operation at least once
		 do{
		 	operations;
		 }while(condition);
		 
		 */
		
		int i=1;

		while(i>5) {
			System.out.println("hello");
		}
		
		do{
			System.out.println("hola");
		}while(i>5);
		
	}

}











